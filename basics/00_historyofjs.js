/*
  HISTORY OF JAVASCRIPT — DETAILED EXPLANATION
  ---------------------------------------------
  This timeline explains how JavaScript was created, how it evolved,
  and how it became the most popular language for the web.
*/


/* ============================================================
   1. THE BEGINNING (1995)
   ============================================================
   - Netscape Navigator was the most popular web browser.
   - The web was mostly static (HTML only).
   - Netscape wanted a scripting language to make pages interactive.
   - Brendan Eich was hired at Netscape.
   - He created JavaScript in JUST 10 DAYS (May 1995).
   - The original name was:
        → Mocha
        → then LiveScript
        → finally renamed to JavaScript for marketing reasons.
   - The name "JavaScript" was chosen to ride on Java’s popularity.
*/



/* ============================================================
   2. EARLY STANDARDIZATION (1996–1997)
   ============================================================
   - Microsoft created JScript for Internet Explorer (IE).
   - Browser wars began, and JS behavior differed in each browser.
   - To fix this, JavaScript needed a standard.
   - ECMA International standardized the language in 1997.
   - The official standard became:
        → ECMAScript (ES)
   - JavaScript is an IMPLEMENTATION of ECMAScript.
*/



/* ============================================================
   3. ECMAScript VERSIONS & MAJOR MILESTONES
   ============================================================
   ES1 (1997)
     - First official specification.

   ES2 (1998)
     - Minor fixes.

   ES3 (1999)
     - Big update.
     - Features like: regex, try/catch, switch, do-while.

   ES4 (2000–2008)
     - Planned huge update (classes, modules, types).
     - CANCELLED due to disagreements.
*/



/* ============================================================
   4. THE "DARK AGE" & JQUERY ERA (2005–2008)
   ============================================================
   - Browser incompatibilities made JS hard to use.
   - jQuery (2006) became extremely popular.
   - Developers relied on jQuery to write cross-browser code.
*/



/* ============================================================
   5. ES5 REVOLUTION (2009)
   ============================================================
   - ES5 brought stability and important features:
       → strict mode
       → JSON support
       → Array methods: map, filter, reduce
       → Object.defineProperty
   - This made JS suitable for serious application development.
*/



/* ============================================================
   6. ES6 / ES2015 — THE BIGGEST UPDATE IN JS HISTORY
   ============================================================
   - Released in 2015.
   - Modern JavaScript begins here.
   - Major features:
     → let & const
     → arrow functions
     → classes
     → promises
     → template literals
     → destructuring
     → spread/rest operators
     → modules (import/export)
   - ES6 transformed JS into a powerful, modern language.
*/



/* ============================================================
   7. YEARLY RELEASES (ES2016 → PRESENT)
   ============================================================
   - After ES6, JavaScript switched to yearly updates.
   - Some important additions:
       ES2016 → Array.includes, exponent operator
       ES2017 → async/await
       ES2018 → rest/spread for objects, async iterations
       ES2019 → flat(), flatMap()
       ES2020 → optional chaining, nullish coalescing
       ES2021 → logical assignment, promise.any
       ES2022 → class fields, static initialization blocks
*/



/* ============================================================
   8. RISE OF NODE.JS (2009)
   ============================================================
   - Ryan Dahl created Node.js using Google’s V8 engine.
   - JS could now run on servers.
   - Result:
       → Full-stack JavaScript development became possible.
       → npm ecosystem exploded.
*/



/* ============================================================
   9. MODERN JAVASCRIPT ECOSYSTEM
   ============================================================
   - Frameworks:
       → React (2013)
       → Angular (2016)
       → Vue (2014)
   - Build tools:
       → Webpack, Babel, Vite, Rollup
   - TypeScript popularity increased (2012 → now huge).
   - JavaScript now powers:
       → web apps
       → mobile apps (React Native)
       → desktop apps (Electron)
       → backend servers (Node.js)
       → game development
*/



/* ============================================================
   10. SUMMARY OF JS EVOLUTION
   ============================================================
   - 1995: Created in 10 days by Brendan Eich.
   - 1997: Standardized as ECMAScript.
   - 2000s: jQuery era, browser wars.
   - 2009: ES5 stabilizes the language.
   - 2009: Node.js brings JS to servers.
   - 2015 (ES6): JavaScript becomes modern and powerful.
   - 2016–now: Continuous annual updates.
   - Today: One of the most important languages in the world.
*/
